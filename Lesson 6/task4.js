/**
 * Напишете програма, която сортира (подрежда 
 * елементите във
   възходящ ред) масив, съдържащ само 0 и 1.
 */

let arr = [0,1,1,1,0,0,1,0,1,0,1,1,1];


for(let i=0; i< arr.length; i++) {
    if(arr[i] === 0) {
        arr.unshift(arr[i]);
        arr.splice(i+1,1);
    } 
}

